package enumeration;

/**
 * Enum for player colours.
 * This enum is useful when we want to check things depending on the player.
 */
public enum Color {
    /**
     * Yellow colour.
     */
    YELLOW,
    /**
     * Blue colour.
     */
    BLUE,
    /**
     * Red colour.
     */
    RED,
    /**
     * Green colour.
     */
    GREEN
}
