package enumeration;

/**
 * Enum for directions.
 * This enum is useful when we check if a move is possible, or if a player can place a barrier.
 */
public enum Direction {
    /**
     * North direction.
     */
    NORTH,
    /**
     * East direction.
     */
    EAST,
    /**
     * South direction.
     */
    SOUTH,
    /**
     * West direction.
     */
    WEST
}
